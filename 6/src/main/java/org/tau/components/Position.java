package org.tau.components;

public class Position {
    private int row = 0;
    private int col = 0;

    public int getRow() {
        return row;
    }

    void setRow(int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    void setCol(int col) {
        this.col = col;
    }
}
